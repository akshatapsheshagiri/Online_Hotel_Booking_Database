	GUEST_ID INT,
	ROOM_ID VARCHAR(255), 
    PAYMENT_ID INT,
	BOOKING_FROM_DATE DATE,
	BOOKING_TO_DATE DATE,
	NUMBER_OF_GUESTS INT,
